// SUM OF A SERIES OF MULTIBYTE DECIMAL NUMBERS
# ORG 2400H
# BEGIN 2400H
	   LDA COUNTER
	   MOV B,A
	   LXI D,2601

LOOP:	   LXI H,SIZE
	   MOV C,M
	   LXI H,SUM
	   ORA A

BACK:	   LDAX D
	   ADC M
	   DAA
	   MOV M,A
	   INX D
	   INX H
	   DCR C
	   JNZ BACK
	   DCR B
	   JNZ LOOP
	   HLT

// COUNT FOR THE LENGTH OF SERIES
# ORG 2500H
# COUNTER:  DB 03H

// COUNT FOR THE NUMBER OF BYTES PLACED IN THE MEMORY LOCATION
# ORG 2501H
# SIZE: DB 05H

